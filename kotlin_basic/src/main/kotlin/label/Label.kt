package label

fun labelBreak(){
    loopForI@  for (i in 1..10){
        loopForJ@ for(j in 1..10){
            // bisa digunakan untuk break berdasarkan label
            if(i > 5){
                break@loopForI
            }
            println("$i * $j = ${i * j}")


        }
    }
}

fun labelContinue(){
    loopForI@  for (i in 1..10){
        loopForJ@ for(j in 1..10){
            // bisa digunakan untuk break berdasarkan label
            if(j == 5){
                continue@loopForI
            }
            println("$i * $j = ${i * j}")


        }
    }
}

